# This will take the input of a shape element, and return an array
# with the id, then amounts of occurences for 9 different place types
def apiCounter(shape,categories):
  categoryCounts = []
  for i in range(len(categories)):
    # Response count will indicate how many responses we have received
    # Offset will increment by 20 until it is no longer equal to response count
    # In this case, it indicates that the responses have been expended
    responseCount = 0
    offset = 0
    while (responseCount == offset and offset <1000):
      params = get_search_parameters(shape.bbox,categories[i],offset)
      response = len(get_results(params)['businesses'])
      responseCount += response
      offset += 20
      # 
    categoryCounts += [responseCount]
  return categoryCounts



